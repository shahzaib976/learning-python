num2 = 50
optr = "+"

if optr=="+":
    print(num1," + ",num2," = ",num1+num2)
elif optr=="-":
    print(num1," - ",num2," = ",num1-num2)
elif optr=="*":
    print(num1," * ",num2," = ",num1*num2)
elif optr=="/":
    print(num1," / ",num2," = ",num1/num2)
elif optr=="%":
    print(num1," % ",num2," = ",num1%num2)
elif optr=="**": 
    print(num1,"**",num2," = ",num1**num2 )  
else:
    print("Invilid Operater Provided")




